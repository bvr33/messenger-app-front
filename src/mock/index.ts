export interface ChatListData {
  name: string
  msg: string
  avatar: string
}

const normalizedData = chatList.map(({ msg, lastMsgOwnerName, ...data }): ChatListData => {
  const msgPrefix = `${lastMsgOwnerName}: `
  const prefixLength = msgPrefix.length
  const fullMsg = `${msgPrefix}${msg}`
  const fullMsgLength = fullMsg.length
  const length = 35
  if (fullMsgLength > length) {
    return {
      ...data,
      msg: msgPrefix.concat(msg.substring(0, length - prefixLength).concat('...')).trim()
    }
  } else {
    return {
      ...data,
      msg: msgPrefix.concat(msg).trim()
    }
  }
})
export const data = () => {
  return [...normalizedData]
}

